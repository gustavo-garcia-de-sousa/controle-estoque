    private void btnExcluirFornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirFornActionPerformed
        if(JTFornecedor.getSelectedRow() != -1){
            if(JOptionPane.showConfirmDialog(null, "Deseja excluir o registro?", "Confirmação de exclusão!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION){
                Fornecedor f = new Fornecedor();
                FornecedorDAO dao = new FornecedorDAO();
                
                f.setIdFornecedor((int) JTFornecedor.getValueAt(JTFornecedor.getSelectedRow(), 0));
                dao.delete(f);
           }
        }else{
           JOptionPane.showMessageDialog(null, "Selecione um fornecedor para excluir!");
        }
        
        limpaCamposForm();
        readTableFornecedores();
        
    }//GEN-LAST:event_btnExcluirFornActionPerformed

