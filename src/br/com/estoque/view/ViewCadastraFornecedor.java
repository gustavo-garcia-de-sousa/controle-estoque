    private void btnCadFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadFornecedorActionPerformed
        if((txtRazaoSocial.getText().length() <= 0) || (txtCNPJ.getText().length() <= 0) || (txtFoneFixo.getText().length() <= 0) || (txtFoneCelular.getText().length() <= 0)){
            JOptionPane.showMessageDialog(null, "Favor preencher todos os campos obrigatórios");
        }else{
            Fornecedor f = new Fornecedor();
            FornecedorDAO dao = new FornecedorDAO();
            
            f.setRazaoSocial(txtRazaoSocial.getText());
            f.setCNPJ(txtCNPJ.getText());
            f.setIE(txtIE.getText());
            f.setEnd(txtRua.getText());
            f.setBairro(txtBairro.getText());
            f.setCidade(txtCidade.getText());
            f.setUF((String) jcbUF.getSelectedItem());
            f.setFoneFixo(txtFoneFixo.getText());
            f.setFoneCelular(txtFoneCelular.getText());
            f.setEmail(txtEmail.getText());
            f.setResp(txtResponsavel.getText());
            
            dao.create(f);
        }
        
        limpaCamposForm();
        readTableFornecedores();
    }//GEN-LAST:event_btnCadFornecedorActionPerformed

