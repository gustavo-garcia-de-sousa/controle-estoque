    public void readTableFornecedores(){
        DefaultTableModel modeloForn = (DefaultTableModel) JTFornecedor.getModel();
        modeloForn.setNumRows(0);
        FornecedorDAO dao = new FornecedorDAO();
        
        for(Fornecedor f : dao.read()){
            modeloForn.addRow(new Object[]{
                f.getIdFornecedor(),
                f.getRazaoSocial(),
                f.getCNPJ(),
                f.getIE(),
                f.getFoneFixo(),
                f.getFoneCelular(),
                f.getEmail(),
                f.getResp(),
                f.getEnd(),
                f.getBairro(),
                f.getCidade(),
                f.getUF()
            });
        }
    }
    
    public void readTableFornForDesc(String busca){
        DefaultTableModel modeloForn = (DefaultTableModel) JTFornecedor.getModel();
        modeloForn.setNumRows(0);
        FornecedorDAO dao = new FornecedorDAO();
        
        for(Fornecedor f : dao.readForDesc(busca)){
            modeloForn.addRow(new Object[]{
                f.getIdFornecedor(),
                f.getRazaoSocial(),
                f.getCNPJ(),
                f.getIE(),
                f.getFoneFixo(),
                f.getFoneCelular(),
                f.getEmail(),
                f.getResp(),
                f.getEnd(),
                f.getBairro(),
                f.getCidade(),
                f.getUF()
            });
        }
    }
    
