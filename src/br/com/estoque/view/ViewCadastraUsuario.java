    public void readTableUsuarios() {
        DefaultTableModel modelo = (DefaultTableModel) JTUsuarios.getModel();
        modelo.setNumRows(0);
        UsuarioDAO dao = new UsuarioDAO();

        for (Usuario u : dao.read()) {
            modelo.addRow(new Object[]{
                u.getIdUsuario(),
                u.getNome(),
                u.getTipo(),
                u.getLogin(),
                u.getEmail()
            });
        }
    }

