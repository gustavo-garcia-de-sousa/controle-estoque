    private void btnCadUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadUsuarioActionPerformed
        if ((txtLoginUsuario.getText().length() <= 0) || (txtNomeUsuario.getText().length() <= 0) || (txtSenhausuario.getText().length() <= 0) || (txtRepeteSenha.getText().length() <= 0) || (txtEmailUsuario.getText().length() <= 0)) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!");
        } else if (txtSenhausuario.getText() == null ? txtRepeteSenha.getText() != null : !txtSenhausuario.getText().equals(txtRepeteSenha.getText())) {
            JOptionPane.showMessageDialog(null, "As senhas devem ser iguais!");
            txtSenhausuario.setText("");
            txtRepeteSenha.setText("");
        } else {
            Usuario u = new Usuario();
            UsuarioDAO dao = new UsuarioDAO();

            u.setTipo((String) jcbTipoUsuario.getSelectedItem());
            u.setNome(txtNomeUsuario.getText());
            u.setLogin(txtLoginUsuario.getText());
            u.setSenha(txtSenhausuario.getText());
            u.setEmail(txtEmailUsuario.getText());
            dao.create(u);

            jcbTipoUsuario.setSelectedIndex(0);
            txtNomeUsuario.setText("");
            txtLoginUsuario.setText("");
            txtSenhausuario.setText("");
            txtRepeteSenha.setText("");
            txtEmailUsuario.setText("");
        }
        readTableUsuarios();
    }//GEN-LAST:event_btnCadUsuarioActionPerformed

    private void btnExcUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcUsuarioActionPerformed
       
        if(JTUsuarios.getSelectedRow() != -1){
            if(JOptionPane.showConfirmDialog(null, "Deseja excluir o registro?", "Confirmação de exclusão!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION){
                Usuario u = new Usuario();
                UsuarioDAO dao = new UsuarioDAO();

                u.setIdUsuario((int) JTUsuarios.getValueAt(JTUsuarios.getSelectedRow(), 0));
                dao.delete(u);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!");
        }

        jcbTipoUsuario.setSelectedIndex(0);
        txtNomeUsuario.setText("");
        txtLoginUsuario.setText("");
        txtSenhausuario.setText("");
        txtRepeteSenha.setText("");
        txtEmailUsuario.setText("");
        readTableUsuarios();
    }//GEN-LAST:event_btnExcUsuarioActionPerformed

